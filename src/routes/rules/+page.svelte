	<section>
		<h2>Game Overview</h2>

		<p>
			"Rebel City" is a turn-based strategy game set in a modern urban environment where players
			lead a revolutionary movement. The game combines elements of strategic planning, resource
			management, and tactical decision-making. Players work together to build their underground
			base, influence city dynamics, and accomplish personal and collective goals, all while
			avoiding raising suspicion to a critical level.
		</p>
		<p>
			"Rebel City" is a turn-based strategy game set in a modern urban environment, blending
			elements of strategic planning, resource management, and character development. Players lead a
			revolutionary movement, building their underground base, influencing city dynamics, and
			fulfilling individual missions, all while balancing the collective needs and skills of their
			characters.
		</p>
	</section>
	<section>
		<h2>Game Contents</h2>

		<ul>
			<li>
				25 City Map Tiles:
				<ul>
					<li>1 City Hall Tile</li>
					<li>
						8 Inner Ring Tiles (including 2 Alleyway Tiles (Nr. 1 & 2), University and Main Square
						Tile)
					</li>
					<li>
						16 Outer Ring Tiles (including 4 Alleyway Tiles (Nr. 3 - 6), Hospital, Factory and Mall
						Tile)
					</li>
				</ul>
			</li>
			<li>1 City Map Grid (5x5)</li>
			<li>1 Underground Map Grid (25x25)</li>
			<li>1 Six-Sided Die</li>
			<li>X Mission Cards</li>
			<li>X Event Cards</li>
			<li>X Underground Base Tiles</li>
			<li>X Resource Tokens</li>
			<li>X Character Sheets</li>
			<li>Markers for Base Entrance and Revolution, Community and Suspicion Points</li>
		</ul>
	</section>
	<section>
		<h2>Setting Up the City Map</h2>

		<h3 class="subsection-title">Central Administrative District:</h3>

		<p>Place the City Hall tile at the center of the 5x5 grid.</p>
		<h3 class="subsection-title">Inner Ring (Residential and Cultural District):</h3>

		<p>
			Shuffle the Inner Ring tiles, including the Alleyway tiles, the University and the Main
			Square, and lay them face down around the City Hall tile.
		</p>
		<h3 class="subsection-title">Outer Ring (Commercial and Industrial District):</h3>

		<p>
			Shuffle the Outer Ring tiles, including the Alleyway Tiles, the Hospital, Factory and the
			Mall, and lay them face down to form the outer layer of the grid.
		</p>
		<h3 class="subsection-title">Reveal the Cityscape:</h3>

		<p>Turn over all the tiles to reveal the layout of Rebel City.</p>
	</section>
	<section>
		<h2>Setting Up the Underground Base</h2>

		<h3 class="subsection-title">Initial Base Setup:</h3>

		<p>Start with one Underground Base Tile on the Underground Map.</p>
		<h3 class="subsection-title">Scaling:</h3>

		<p>Each surface tile is equivalent to a 5x5 grid of underground tiles.</p>
		<h3 class="subsection-title">Entrance Placement:</h3>

		<p>
			Roll the six-sided die to determine the entrance to the underground base, placing it on the
			corresponding Alleyway tile on the City Map.
		</p>
	</section>
	<section>
		<h2>Starting the Game</h2>

		<h3 class="subsection-title">Initial Setup:</h3>

		<ul>
			<li>Each player selects a character and takes the corresponding character sheet.</li>
			<li>
				Each player draws 1 Mission Card. These cards set individual objectives that players must
				work towards throughout the game.
			</li>
			<li>Distribute a starting set of resource tokens to each player (to be established)</li>
		</ul>
		<h3 class="subsection-title">Turn Structure</h3>

		<p>"Rebel City" is played in turns, each consisting of three distinct phases:</p>
		<h3 class="subsection-title">Base's Turn (Collective Strategy Phase):</h3>

		<ul>
			<li>
				Plan and Build: Players discuss how to upgrade and expand their underground base. Decisions
				might include building new rooms, starting research projects, or planning how to use
				resources.
			</li>
			<li>
				Event Planning: Players can plan events or actions to take place in the city, like
				organizing protests, rallies, or covert operations.
			</li>
			<li>
				Resource Management: Allocate and manage resources essential for the base and various
				activities.
			</li>
		</ul>
		<h3 class="subsection-title">Player's Turn (Individual Action Phase):</h3>

		<ul>
			<li>
				Mission Progress: Players work on their individual Mission Cards. These missions can range
				from personal goals tied to their character's backstory to strategic objectives that benefit
				the revolution.
			</li>
			<li>
				Taking Actions: Players can choose to perform actions like recruiting new members, gathering
				intelligence, or carrying out parts of a larger plan.
			</li>
			<li>
				Collaboration: Players may collaborate on missions or city events, pooling resources and
				strategies.
			</li>
		</ul>
		<h3 class="subsection-title">City's Turn (Reactive Phase):</h3>

		<ul>
			<li>
				Event Resolution: Carry out and resolve any events that were planned during the Base's Turn.
			</li>
			<li>
				Drawing Event Cards: Draw an Event Card to introduce new challenges or opportunities in the
				city. These events can affect everything from the revolution's progress to the city's
				suspicion level.
			</li>
			<li>
				Adjusting Points: Update Revolution Points, Community Points and Suspicion Points based on
				the outcomes of events and actions.
			</li>
		</ul>
		<h3 class="subsection-title">Skills System</h3>

		<p>
			Skills are broadly categorized to reflect different areas of expertise and capabilities that
			are crucial in a revolutionary setting. Each skill category encompasses a range of related
			abilities a character can possess or acquire over time.
		</p>
		<ol>
			<li>
				Technical Skills:
				<ul>
					<li>Includes mechanics, IT, engineering, and other technical abilities.</li>
					<li>
						Useful for tasks like sabotaging systems, hacking, or building and repairing equipment.
					</li>
				</ul>
			</li>
			<li>
				Social Skills:
				<ul>
					<li>Encompasses public speaking, persuasion, teaching, and negotiation.</li>
					<li>Key for recruiting, organizing events, and influencing public opinion.</li>
				</ul>
			</li>
			<li>
				Strategic Skills:
				<ul>
					<li>Involves planning, logistics, and tactical thinking.</li>
					<li>
						Essential for mission planning, resource allocation, and strategic decision-making.
					</li>
				</ul>
			</li>
			<li>
				Medical Skills:
				<ul>
					<li>Covers medical knowledge, first aid, and healthcare.</li>
					<li>
						Important for treating injuries, managing health crises, and maintaining overall group
						health.
					</li>
				</ul>
			</li>
			<li>
				Stealth Skills:
				<ul>
					<li>Includes stealth, espionage, and covert operations.</li>
					<li>
						Crucial for gathering intelligence, conducting secret missions, and avoiding detection.
					</li>
				</ul>
			</li>
		</ol>
		<h3 class="subsection-title">Needs System</h3>

		<p>
			Needs are fundamental requirements that each character must fulfill to maintain efficiency and
			well-being. Neglecting these needs can have consequences on the character's performance and
			mental state.
		</p>
		<ol>
			<li>
				Physical Needs:
				<ul>
					<li>Includes nutrition, sleep, and physical health.</li>
					<li>
						Neglecting physical needs can lead to decreased performance and increased susceptibility
						to illness.
					</li>
				</ul>
			</li>
			<li>
				Psychological Needs:
				<ul>
					<li>Covers mental health, stress management, and morale.</li>
					<li>Essential for maintaining focus, motivation, and decision-making capabilities.</li>
				</ul>
			</li>
			<li>
				Social Needs:
				<ul>
					<li>Involves social interaction, belonging, and teamwork.</li>
					<li>Important for cooperation, morale, and preventing feelings of isolation.</li>
				</ul>
			</li>
			<li>
				Safety Needs:
				<ul>
					<li>Concerns personal safety, security, and protection from harm.</li>
					<li>
						Crucial for ensuring characters feel secure enough to perform their roles effectively.
					</li>
				</ul>
			</li>
			<li>
				Self-Actualization Needs:
				<ul>
					<li>Relates to personal growth, fulfillment of potential, and creativity.</li>
					<li>
						Neglecting this can hinder a character's development and contribution to the revolution.
					</li>
				</ul>
			</li>
		</ol>
		<h3 class="subsection-title">Integration into Gameplay</h3>

		<ul>
			<li>
				Skill Checks: Certain actions or missions may require skill checks, where players must
				utilize their characters' skills to overcome challenges.
			</li>
			<li>
				Needs Fulfillment: Players must manage their characters' needs, balancing their time and
				resources to ensure that no need is severely neglected.
			</li>
			<li>
				Character Development: Characters can develop their skills over time, becoming more
				proficient in certain areas.
			</li>
			<li>
				Impact on Performance: The fulfillment of needs and skill proficiency directly impacts a
				character's effectiveness in missions and tasks.
			</li>
		</ul>
		<h3 class="subsection-title">Community Points</h3>

		<p>
			Community Points (CPs) represent the efforts and achievements in fostering a supportive,
			sustainable, and equitable community within the city. While Revolution Points determine the
			success of overthrowing the existing system, Community Points dictate the quality and
			stability of the post-revolution society.
		</p>
		<h4>Earning Community Points</h4>

		<ol>
			<li>
				Community Building Actions:
				<ul>
					<li>
						Players earn CPs by implementing community-based projects like setting up renewable
						energy resources, organizing educational programs, or creating healthcare initiatives.
					</li>
					<li>
						CPs are also gained through events and actions that promote social equity and communal
						well-being.
					</li>
				</ul>
			</li>
			<li>
				Strategic Considerations:
				<ul>
					<li>
						While planning and executing actions, players must consider the impact on the community.
						Balancing revolutionary activities with community building is key to a successful
						post-revolution society.
					</li>
				</ul>
			</li>
		</ol>
		<h4>Tracking Community Points</h4>

		<ul>
			<li>
				Community Points Scale: Similar to Revolution and Suspicion Points, CPs are tracked on a
				scale from 0 to 10.
			</li>
			<li>
				Milestones: Reaching certain levels on the CP scale can unlock additional benefits or
				challenges, reflecting the growing strength and resilience of the community.
			</li>
		</ul>
		<h4>Endgame and Determining the Outcome</h4>

		<ul>
			<li>
				Overthrowing the System: Achieving 10 Revolution Points still signifies the successful
				overthrow of the existing system.
			</li>
			<li>
				Determining the Aftermath:
				<ul>
					<li>
						The Community Points score at the end of the game determines the nature of the
						post-revolution society.
					</li>
					<li>
						A high CP score indicates a thriving, well-prepared society post-revolution, leading to
						a more stable and prosperous outcome.
					</li>
					<li>
						A low CP score suggests that while the revolution was successful, the lack of community
						groundwork might result in chaos or an unstable society.
					</li>
				</ul>
			</li>
		</ul>
		<h4>Impact on Gameplay</h4>

		<ul>
			<li>
				Balanced Strategy: Players must strategize to balance the accumulation of Revolution Points
				with the nurturing of the community, as neglecting either aspect can have significant
				consequences.
			</li>
			<li>
				Collaborative Decisions: The introduction of CPs encourages players to collaborate and make
				decisions that benefit not just the revolution but also the society as a whole.
			</li>
			<li>
				Narrative Depth: CPs add a layer of narrative depth to the game, emphasizing that a
				successful revolution is not just about overthrowing a regime but also about building a
				better future.
			</li>
		</ul>
		<h3 class="subsection-title">Progressing Through the Game</h3>

		<ul>
			<li>
				Completing Missions: After completing a mission, a player draws a new Mission Card,
				continuing their personal narrative within the game. Successfully completed missions
				contribute to the collective goal of accruing Revolution Points but might also raise
				Suspicion Points.
			</li>
			<li>
				Milestones: Keep track of milestones on the Revolution, Community and Suspicion scales.
				Certain events are triggered when reaching 3, 6, and 9 points on either scale, influencing
				the game's direction and difficulty.
			</li>
		</ul>
		<h3 class="subsection-title">Winning and Losing</h3>

		<ul>
			<li>
				Victory: The game ends when players collectively reach 10 Revolution Points, successfully
				building a strong enough movement to effect change in the city. The Community Points score
				at game end determines the quality of the post-revolution society.
			</li>
			<li>
				Defeat: The game is lost if Suspicion Points reach 10, indicating that the movement has been
				compromised or crushed by city authorities.
			</li>
		</ul>
